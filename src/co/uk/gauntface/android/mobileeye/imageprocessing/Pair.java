package co.uk.gauntface.android.mobileeye.imageprocessing;

public class Pair
{
	private Object mArg1;
	private Object mArg2;
	
	public Pair(Object arg1, Object arg2)
	{
		mArg1 = arg1;
		mArg2 = arg2;
	}
	
	public Object getArg1()
	{
		return mArg1;
	}
	
	public Object getArg2()
	{
		return mArg2;
	}
}
